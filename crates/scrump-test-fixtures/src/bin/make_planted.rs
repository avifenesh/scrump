//! Emit the Phase 0 planted-token fixture to a path.
//!
//! Generated each test run from `scrump_test_fixtures::tokens`. No
//! checked-in source holds a contiguous token-shaped literal.
//!
//! Usage: `make-planted <output_path>`

use scrump_test_fixtures::tokens;

fn main() {
    let out = match std::env::args().nth(1) {
        Some(p) => p,
        None => {
            eprintln!("usage: make-planted <out>");
            std::process::exit(2);
        }
    };
    let body = build();
    std::fs::write(&out, body).expect("write");
    eprintln!("wrote {out}");
}

fn build() -> String {
    let mut s = String::new();
    s.push_str("# Phase 0 planted fixture, generated by make-planted.\n");
    s.push_str("# Every value below is an obvious-fake placeholder composed\n");
    s.push_str("# from non-contiguous source fragments — no contiguous token\n");
    s.push_str("# shape appears in any checked-in source.\n\n");
    s.push_str(&format!("export GH_TOKEN={}\n", tokens::ghp()));
    s.push_str(&format!("export HF_TOKEN={}\n", tokens::hf()));
    s.push_str(&format!(
        "export ANTHROPIC_API_KEY={}\n",
        tokens::anthropic()
    ));
    s.push_str(&format!("export AWS_ACCESS_KEY_ID={}\n", tokens::aws()));
    s.push_str(&format!("export GOOGLE_API_KEY={}\n", tokens::google()));
    s.push_str(&format!("export SLACK_BOT_TOKEN={}\n", tokens::slack()));
    s.push_str(&format!("export NGC_API_KEY={}\n", tokens::ngc()));
    s.push_str(&format!("export WANDB_API_KEY={}\n", tokens::wandb()));
    s
}
