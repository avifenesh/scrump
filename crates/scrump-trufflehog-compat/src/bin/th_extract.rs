//! Walk every detector directory under `vendor/trufflehog/pkg/detectors/`,
//! extract each `var name = regexp.MustCompile(...)` body, resolve
//! `detectors.PrefixRegex([]string{"a", "b"})` helper calls, and emit:
//!
//!   * `crates/scrump-rules/rules/trufflehog.yaml` — every pattern as a
//!     scrump rule with id `<provider>__<patname>`.
//!   * `crates/scrump-trufflehog-compat/data/provider_map.json` — map
//!     provider → list of generated rule ids, consumed by the compat
//!     harness for auto-discovery.
//!
//! Output is regex::bytes-flavoured (Rust crate `regex`); since TruffleHog
//! itself uses RE2-compatible regex (Go std / wasilibs-go-re2), the source
//! patterns drop in verbatim.

use std::collections::{BTreeMap, BTreeSet};
use std::fs;
use std::path::{Path, PathBuf};

use serde::Serialize;

#[derive(Serialize)]
struct Rule {
    id: String,
    pattern: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    capture_index: Option<usize>,
    #[serde(skip_serializing_if = "Option::is_none")]
    min_entropy: Option<f64>,
}

#[derive(Serialize)]
struct RulesFile {
    rules: Vec<Rule>,
}

fn main() -> std::io::Result<()> {
    let root = workspace_root();
    let detectors_dir = root.join("vendor/trufflehog/pkg/detectors");
    if !detectors_dir.exists() {
        eprintln!(
            "missing {} — run the trufflehog clone first",
            detectors_dir.display()
        );
        std::process::exit(2);
    }

    // Pre-scan `pkg/common/` for shared regex constants (e.g. EmailPattern).
    let common_consts = load_common_consts(&root.join("vendor/trufflehog/pkg/common"));

    let mut rules: Vec<Rule> = Vec::new();
    let mut map: BTreeMap<String, Vec<String>> = BTreeMap::new();
    let mut seen_ids: BTreeSet<String> = BTreeSet::new();
    let mut parsed_files = 0usize;
    let mut skipped_complex = 0usize;

    for provider_dir in walk_providers(&detectors_dir) {
        let provider = provider_dir
            .strip_prefix(&detectors_dir)
            .unwrap()
            .to_string_lossy()
            .replace(['/', '\\'], "_");
        for go_file in find_main_files(&provider_dir) {
            let src = match fs::read_to_string(&go_file) {
                Ok(s) => s,
                Err(_) => continue,
            };
            parsed_files += 1;
            for (name, raw_pattern) in extract_patterns(&src) {
                let resolved = match resolve_prefix_regex(&raw_pattern, &common_consts) {
                    Some(r) => r,
                    None => {
                        skipped_complex += 1;
                        continue;
                    }
                };
                // Compile-validate via Rust's regex crate.
                let compiled = match regex::bytes::Regex::new(&resolved) {
                    Ok(c) => c,
                    Err(_) => {
                        skipped_complex += 1;
                        continue;
                    }
                };
                let cap_idx = if compiled.captures_len() > 1 {
                    Some(1)
                } else {
                    None
                };
                let id = format!("{}__{}", provider, name);
                if seen_ids.insert(id.clone()) {
                    rules.push(Rule {
                        id: id.clone(),
                        pattern: resolved,
                        capture_index: cap_idx,
                        min_entropy: None,
                    });
                    map.entry(provider.clone()).or_default().push(id);
                }
            }
        }
    }

    // ---- Write outputs -----------------------------------------------------
    let yaml_path = root.join("crates/scrump-rules/rules/trufflehog.yaml");
    let yaml_body = serde_yaml::to_string(&RulesFile {
        rules: rules.into_iter().collect(),
    })
    .map_err(|e| std::io::Error::other(format!("yaml serialize: {e}")))?;
    let header = "# Auto-generated by `cargo run -p scrump-trufflehog-compat --bin th-extract`.\n\
         # Do not edit by hand. Re-run the extractor to refresh.\n";
    fs::write(&yaml_path, format!("{header}{yaml_body}"))?;

    let map_path = root.join("crates/scrump-trufflehog-compat/data/provider_map.json");
    if let Some(p) = map_path.parent() {
        fs::create_dir_all(p)?;
    }
    let map_body = serde_json::to_string_pretty(&map)
        .map_err(|e| std::io::Error::other(format!("json serialize: {e}")))?;
    fs::write(&map_path, map_body)?;

    eprintln!(
        "Extracted: {} rules from {} providers (parsed {} files, skipped {} complex)",
        map.values().map(Vec::len).sum::<usize>(),
        map.len(),
        parsed_files,
        skipped_complex
    );
    eprintln!("  rules:    {}", yaml_path.display());
    eprintln!("  provider: {}", map_path.display());
    Ok(())
}

fn workspace_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .unwrap()
        .parent()
        .unwrap()
        .to_path_buf()
}

/// Yield every directory inside `root` that contains at least one `.go`
/// file. Recurses one level deep so `github/v2/` is visited too.
fn walk_providers(root: &Path) -> Vec<PathBuf> {
    let mut out = Vec::new();
    walk_recursive(root, 0, &mut out);
    out.sort();
    out
}

fn walk_recursive(dir: &Path, depth: usize, out: &mut Vec<PathBuf>) {
    if depth > 3 {
        return;
    }
    let Ok(entries) = fs::read_dir(dir) else {
        return;
    };
    let mut has_go = false;
    for e in entries.flatten() {
        let p = e.path();
        if p.is_dir() {
            walk_recursive(&p, depth + 1, out);
        } else if p.extension().is_some_and(|ext| ext == "go") {
            has_go = true;
        }
    }
    // Skip the top-level `pkg/detectors` dir itself (depth 0).
    if has_go && depth > 0 {
        out.push(dir.to_path_buf());
    }
}

fn find_main_files(dir: &Path) -> Vec<PathBuf> {
    let mut out = Vec::new();
    let Ok(entries) = fs::read_dir(dir) else {
        return out;
    };
    for e in entries.flatten() {
        let p = e.path();
        let Some(name) = p.file_name().and_then(|s| s.to_str()) else {
            continue;
        };
        if !name.ends_with(".go") {
            continue;
        }
        if name.ends_with("_test.go")
            || name.ends_with("_integration_test.go")
            || name.ends_with("_benchmark.go")
            || name == "common.go"
            || name == "utils.go"
        {
            continue;
        }
        out.push(p);
    }
    out
}

/// Pull every `name = regexp.MustCompile(<expr>)` pair out of a Go source
/// file. The names get sanitised to lowercase-only-with-underscores.
/// Skips line and block comments so commented-out patterns don't surface.
fn extract_patterns(src: &str) -> Vec<(String, String)> {
    let bytes = src.as_bytes();
    let mut out = Vec::new();
    let mut i = 0;
    while i < bytes.len() {
        // Skip line comments.
        if i + 1 < bytes.len() && bytes[i] == b'/' && bytes[i + 1] == b'/' {
            while i < bytes.len() && bytes[i] != b'\n' {
                i += 1;
            }
            continue;
        }
        // Skip block comments.
        if i + 1 < bytes.len() && bytes[i] == b'/' && bytes[i + 1] == b'*' {
            i += 2;
            while i + 1 < bytes.len() && !(bytes[i] == b'*' && bytes[i + 1] == b'/') {
                i += 1;
            }
            i += 2;
            continue;
        }
        // Skip Go strings so `/*` inside them is ignored.
        if bytes[i] == b'`' {
            i += 1;
            while i < bytes.len() && bytes[i] != b'`' {
                i += 1;
            }
            i += 1;
            continue;
        }
        if bytes[i] == b'"' {
            i += 1;
            while i < bytes.len() && bytes[i] != b'"' {
                if bytes[i] == b'\\' && i + 1 < bytes.len() {
                    i += 2;
                    continue;
                }
                i += 1;
            }
            i += 1;
            continue;
        }
        // Look for `regexp.MustCompile(`.
        let needle = b"regexp.MustCompile(";
        if i + needle.len() > bytes.len() {
            break;
        }
        if &bytes[i..i + needle.len()] != needle {
            i += 1;
            continue;
        }
        // Walk back to find the assignment name on the same logical line.
        let name = backscan_name(bytes, i);
        // Walk forward to find the matching `)` and collect the inner expr.
        let inner_start = i + needle.len();
        let mut depth = 1i32;
        let mut j = inner_start;
        while j < bytes.len() && depth > 0 {
            match bytes[j] {
                b'(' => depth += 1,
                b')' => depth -= 1,
                b'`' => {
                    j += 1;
                    while j < bytes.len() && bytes[j] != b'`' {
                        j += 1;
                    }
                }
                b'"' => {
                    j += 1;
                    while j < bytes.len() && bytes[j] != b'"' {
                        if bytes[j] == b'\\' && j + 1 < bytes.len() {
                            j += 2;
                            continue;
                        }
                        j += 1;
                    }
                }
                _ => {}
            }
            if depth == 0 {
                break;
            }
            j += 1;
        }
        if depth == 0 {
            let expr = std::str::from_utf8(&bytes[inner_start..j]).unwrap_or("");
            if let Some(n) = name {
                out.push((sanitise_name(&n), expr.trim().to_string()));
            }
            i = j + 1;
        } else {
            i = inner_start;
        }
    }
    out
}

fn backscan_name(b: &[u8], i: usize) -> Option<String> {
    // Walk back over whitespace and `=` or `:=`; then capture the identifier.
    let mut k = i;
    while k > 0 && b[k - 1].is_ascii_whitespace() {
        k -= 1;
    }
    if k == 0 {
        return None;
    }
    if b[k - 1] != b'=' {
        return None;
    }
    k -= 1;
    if k > 0 && b[k - 1] == b':' {
        k -= 1;
    }
    while k > 0 && b[k - 1].is_ascii_whitespace() {
        k -= 1;
    }
    let end = k;
    while k > 0 && (b[k - 1].is_ascii_alphanumeric() || b[k - 1] == b'_') {
        k -= 1;
    }
    if k == end {
        return None;
    }
    let name = std::str::from_utf8(&b[k..end]).ok()?.to_string();
    Some(name)
}

fn sanitise_name(s: &str) -> String {
    s.chars()
        .map(|c| {
            if c.is_ascii_alphanumeric() {
                c.to_ascii_lowercase()
            } else {
                '_'
            }
        })
        .collect()
}

/// Resolve a `regexp.MustCompile(...)` body into a regex string.
///
/// Supports:
///   `\`raw\``
///   `detectors.PrefixRegex([]string{"a","b"}) + \`tail\``
///   `\`head\` + detectors.PrefixRegex([]string{"a"}) + \`tail\``
///   `common.X` references resolved against the `common_consts` map.
///
/// Returns `None` for anything that mixes in non-literal identifiers or
/// function calls we don't understand (those go through a hand-port path).
fn resolve_prefix_regex(
    expr: &str,
    common: &std::collections::HashMap<String, String>,
) -> Option<String> {
    let mut out = String::new();
    let bytes = expr.as_bytes();
    let mut i = 0;
    while i < bytes.len() {
        while i < bytes.len() && bytes[i].is_ascii_whitespace() {
            i += 1;
        }
        if i >= bytes.len() {
            break;
        }
        if bytes[i] == b'`' {
            let mut k = i + 1;
            while k < bytes.len() && bytes[k] != b'`' {
                k += 1;
            }
            out.push_str(std::str::from_utf8(&bytes[i + 1..k]).ok()?);
            i = k + 1;
        } else if expr[i..].starts_with("detectors.PrefixRegex(") {
            let args_start = i + "detectors.PrefixRegex(".len();
            // Inside: []string{"a","b"}
            let kws = parse_prefix_keywords(&bytes[args_start..])?;
            // Skip until matching `)`.
            let mut depth = 1i32;
            let mut k = args_start;
            while k < bytes.len() && depth > 0 {
                match bytes[k] {
                    b'(' => depth += 1,
                    b')' => depth -= 1,
                    b'`' => {
                        k += 1;
                        while k < bytes.len() && bytes[k] != b'`' {
                            k += 1;
                        }
                    }
                    b'"' => {
                        k += 1;
                        while k < bytes.len() && bytes[k] != b'"' {
                            if bytes[k] == b'\\' && k + 1 < bytes.len() {
                                k += 2;
                                continue;
                            }
                            k += 1;
                        }
                    }
                    _ => {}
                }
                if depth == 0 {
                    break;
                }
                k += 1;
            }
            if depth != 0 {
                return None;
            }
            // TruffleHog's PrefixRegex treats keywords as raw regex
            // fragments (no escaping). Some detectors exploit this
            // deliberately, e.g. `scraping[ _-]?bee`.
            out.push_str("(?i:");
            out.push_str(&kws.join("|"));
            out.push_str(r")(?:.|[\n\r]){0,40}?");
            i = k + 1;
        } else if expr[i..].starts_with("common.") {
            // common.X — look up in our pre-scanned consts.
            let after = i + "common.".len();
            let id_start = after;
            let mut id_end = id_start;
            while id_end < bytes.len()
                && (bytes[id_end].is_ascii_alphanumeric() || bytes[id_end] == b'_')
            {
                id_end += 1;
            }
            let ident = std::str::from_utf8(&bytes[id_start..id_end]).ok()?;
            let val = common.get(ident)?;
            out.push_str(val);
            i = id_end;
        } else if bytes[i] == b'+' {
            i += 1;
        } else if i + 1 < bytes.len() && bytes[i] == b'/' && bytes[i + 1] == b'/' {
            while i < bytes.len() && bytes[i] != b'\n' {
                i += 1;
            }
        } else {
            // Unknown identifier / call — bail.
            return None;
        }
    }
    if out.is_empty() {
        None
    } else {
        Some(out)
    }
}

/// Pre-scan `pkg/common/*.go` for `const Name = \`...\`` and `const Name =
/// "..."` declarations.
fn load_common_consts(dir: &Path) -> std::collections::HashMap<String, String> {
    let mut out = std::collections::HashMap::new();
    let Ok(entries) = fs::read_dir(dir) else {
        return out;
    };
    for e in entries.flatten() {
        let p = e.path();
        if !p.extension().is_some_and(|x| x == "go")
            || p.file_name()
                .is_some_and(|f| f.to_string_lossy().contains("_test"))
        {
            continue;
        }
        let Ok(src) = fs::read_to_string(&p) else {
            continue;
        };
        scan_consts(&src, &mut out);
    }
    out
}

fn scan_consts(src: &str, out: &mut std::collections::HashMap<String, String>) {
    let bytes = src.as_bytes();
    let mut i = 0;
    while i < bytes.len() {
        // Recognise both `const Name = …` at top-level and inside `const ( … )`.
        if bytes[i..].starts_with(b"const ") || bytes[i..].starts_with(b"const\t") {
            let mut j = i + "const".len();
            j = skip_ws(bytes, j);
            if j < bytes.len() && bytes[j] == b'(' {
                // Block form.
                j += 1;
                while j < bytes.len() && bytes[j] != b')' {
                    j = skip_ws(bytes, j);
                    if j >= bytes.len() || bytes[j] == b')' {
                        break;
                    }
                    consume_const_line(bytes, &mut j, out);
                }
                if j < bytes.len() {
                    j += 1;
                }
                i = j;
                continue;
            } else {
                consume_const_line(bytes, &mut j, out);
                i = j;
                continue;
            }
        }
        i += 1;
    }
}

fn skip_ws(b: &[u8], mut i: usize) -> usize {
    while i < b.len() && b[i].is_ascii_whitespace() {
        i += 1;
    }
    i
}

fn consume_const_line(
    b: &[u8],
    i: &mut usize,
    out: &mut std::collections::HashMap<String, String>,
) {
    *i = skip_ws(b, *i);
    let name_start = *i;
    while *i < b.len() && (b[*i].is_ascii_alphanumeric() || b[*i] == b'_') {
        *i += 1;
    }
    let name = std::str::from_utf8(&b[name_start..*i])
        .unwrap_or("")
        .to_string();
    *i = skip_ws(b, *i);
    if *i >= b.len() || b[*i] != b'=' {
        // Skip to next newline.
        while *i < b.len() && b[*i] != b'\n' {
            *i += 1;
        }
        return;
    }
    *i += 1;
    *i = skip_ws(b, *i);
    let val = if *i < b.len() && b[*i] == b'`' {
        let start = *i + 1;
        let mut k = start;
        while k < b.len() && b[k] != b'`' {
            k += 1;
        }
        let v = std::str::from_utf8(&b[start..k]).unwrap_or("").to_string();
        *i = k + 1;
        Some(v)
    } else if *i < b.len() && b[*i] == b'"' {
        let start = *i + 1;
        let mut k = start;
        while k < b.len() && b[k] != b'"' {
            if b[k] == b'\\' && k + 1 < b.len() {
                k += 2;
                continue;
            }
            k += 1;
        }
        let v = std::str::from_utf8(&b[start..k]).unwrap_or("").to_string();
        *i = k + 1;
        Some(v)
    } else {
        // Non-literal RHS — skip rest of line.
        while *i < b.len() && b[*i] != b'\n' {
            *i += 1;
        }
        None
    };
    if let Some(v) = val {
        if !name.is_empty() {
            out.insert(name, v);
        }
    }
    // Skip optional trailing comment / newline.
    while *i < b.len() && b[*i] != b'\n' {
        *i += 1;
    }
}

fn parse_prefix_keywords(buf: &[u8]) -> Option<Vec<String>> {
    // Expect: `[]string{"a", "b", ...}`
    let mut i = 0;
    while i < buf.len() && buf[i].is_ascii_whitespace() {
        i += 1;
    }
    if !buf[i..].starts_with(b"[]string{") {
        return None;
    }
    i += b"[]string{".len();
    let mut out = Vec::new();
    while i < buf.len() {
        while i < buf.len() && (buf[i].is_ascii_whitespace() || buf[i] == b',') {
            i += 1;
        }
        if i >= buf.len() {
            break;
        }
        if buf[i] == b'}' {
            return Some(out);
        }
        if buf[i] != b'"' {
            return None;
        }
        i += 1;
        let start = i;
        while i < buf.len() && buf[i] != b'"' {
            if buf[i] == b'\\' && i + 1 < buf.len() {
                i += 2;
                continue;
            }
            i += 1;
        }
        let s = std::str::from_utf8(&buf[start..i]).ok()?.to_string();
        // TruffleHog joins keywords with `|` and does NOT escape — some
        // detectors deliberately pass regex fragments like `scraping[ _-]?bee`.
        out.push(s);
        i += 1;
    }
    None
}
